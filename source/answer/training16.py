# 複数の値をまとめて扱うこと時に便利。
# 数値だけを格納したリスト
number = [11,42,64,3,5,2,54,28,69]
print(number)
# 文字列を格納したリスト
colors = ["red", "green", "blue"]
print(colors)
# データ型が混在しているリスト
mix = ["鈴木", 30, "blue", 10.43]
print(mix)
# 空のリストを定義する
empty = []
print(empty)

# リストに文字列を入れると、一文字ずつ分解される
mozi_list = list("Happy hucking python")
print(mozi_list)